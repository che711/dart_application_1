void main() {
  // create a list
  // все в квадратных скобках литералом списка:
  var fruits = ['apples', 'oranges', 'bananas', 'watermelon']; 
  print(fruits);
  print('');

  print(fruits.first);
  print('');

  print(fruits.last);
  print('');

  print(fruits[0]);
  print('');

  print('print(fruits.length - 1):');
  print(fruits.length - 1);
  print('');

  print(fruits.isEmpty);
  print('');

  print(fruits.isNotEmpty);
  print('');
}
